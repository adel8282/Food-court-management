    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed
                                           
    DefaultTableModel modelSource = (DefaultTableModel) jTable3.getModel(); // Source table model
    DefaultTableModel modelTarget = (DefaultTableModel) jTable4.getModel(); // Target table model

    for (int row = 0; row < modelSource.getRowCount(); row++) {
        boolean isRowEmpty = true;

        // Check if the row is empty in jTable4
        for (int col = 0; col < modelTarget.getColumnCount(); col++) {
            if (modelTarget.getRowCount() > row && modelTarget.getValueAt(row, col) != null && 
                !modelTarget.getValueAt(row, col).toString().trim().isEmpty()) {
                isRowEmpty = false;
                break;
            }
        }

        Object[] rowData = new Object[modelSource.getColumnCount()];
        for (int col = 0; col < modelSource.getColumnCount(); col++) {
            rowData[col] = modelSource.getValueAt(row, col); // Extract data
        }

        if (isRowEmpty && modelTarget.getRowCount() > row) {
            // If the row is empty, overwrite it
            for (int col = 0; col < modelSource.getColumnCount(); col++) {
                modelTarget.setValueAt(rowData[col], row, col);
            }
        } else {
            // If the row is not empty or doesn't exist, add a new row
            modelTarget.addRow(rowData);
        }
    }

    // Clear content of jTable3 without deleting rows
    for (int row = 0; row < modelSource.getRowCount(); row++) {
        for (int col = 0; col < modelSource.getColumnCount(); col++) {
            modelSource.setValueAt("", row, col); // Set each cell to empty
        }
    }


        // TODO add your handling code here:
    }//GEN-LAST:event_jToggleButton3ActionPerformed

