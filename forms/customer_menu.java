    private void jToggleButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton6ActionPerformed
                                                                                          
    DefaultTableModel modelSource = (DefaultTableModel) jTable6.getModel(); // Source table model
    DefaultTableModel modelTarget = (DefaultTableModel) reviewcomplainttable.getModel(); // Target table model

    JSONArray complaintsArray = loadComplaintsFromFile(); // Load existing complaints from data.txt

    boolean hasData = false; // Variable to check if there is data in the table

    for (int row = 0; row < modelSource.getRowCount(); row++) {
        if (modelSource.getValueAt(row, 0) != null && !modelSource.getValueAt(row, 0).toString().trim().isEmpty()) {
            hasData = true;
            // Generate a unique Complaint_ID
            int complaintID = generateComplaintID(complaintsArray);

            // Extract data from jTable6
            String userName = modelSource.getValueAt(row, 0).toString();
            String complaintText = modelSource.getValueAt(row, 1).toString();
            String dateSubmitted = modelSource.getValueAt(row, 2).toString();

            // Create a new complaint object
            JSONObject complaint = new JSONObject();
            complaint.put("Complaint_ID", complaintID);
            complaint.put("User_Name", userName);
            complaint.put("Complaint_Text", complaintText);
            complaint.put("Date_Submitted", dateSubmitted);

            // Add the complaint to the JSON array
            complaintsArray.add(complaint);

            // Add the complaint to reviewcomplainttable
            Object[] rowData = {complaintID, userName, complaintText, dateSubmitted};
            modelTarget.addRow(rowData);
        }
    }

    if (!hasData) {
        JOptionPane.showMessageDialog(this, "No complaints to submit. Please enter data before submitting.", "Warning", JOptionPane.WARNING_MESSAGE);
        return; // Stop execution if the table is empty
    }

    // Save updated complaints list back to data.txt
    saveComplaintsToFile(complaintsArray);

    // Clear jTable6 without deleting rows
    for (int row = 0; row < modelSource.getRowCount(); row++) {
        for (int col = 0; col < modelSource.getColumnCount(); col++) {
            modelSource.setValueAt("", row, col); // Set each cell to empty
        }
    }

    // Show success message
    JOptionPane.showMessageDialog(this, "Complaint submitted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);



 // TODO add your handling code here:
    }//GEN-LAST:event_jToggleButton6ActionPerformed

